let news_items = document.querySelector(".news__items");

function setData(id) {
  
  for(let i=0; i<newsItems.length; i++) {
    const div = document.createElement('div');
    div.setAttribute('class', 'item text-color-dark-gray font-size-20 font-weight-700 padding-20-12-20-10 border-bottom-grey');
    div.setAttribute('id', `${i}`);
    div.textContent = newsItems[i].text;
    news_items.appendChild(div);
  }
  let items = document.querySelectorAll(".item");


  for(let i=0; i<items.length; i++) {
    items[i].addEventListener("click", ()=>changeNews(i));
  }

  setLeftData(0)

}

function setLeftData(id) {

  let news = document.querySelector(".news");
  news.innerHTML = "";
  
  let newsImage = document.createElement("div");
  newsImage.setAttribute('class', 'news__image');
  
  let img = document.createElement('img');
  img.setAttribute('src', `./assets/images/${newsDatas[id].img}`);
  img.setAttribute('class', 'image__element');
  newsImage.appendChild(img);
  news.appendChild(newsImage);

  
  let title = document.createElement('h3');
  title.setAttribute('class', 'news__heading text-color-dark-gray font-size-20 font-weight-700 margin-bottom-18');
  title.textContent = newsDatas[id].title;
  news.appendChild(title);
  
  let date = document.createElement('div');
  date.setAttribute('class', 'date font-size-12 text-color-dark-gray margin-bottom-10');
  date.textContent = newsDatas[id].date;
  news.appendChild(date);

  let text = document.createElement('p');
  text.setAttribute('class', 'text-color-dark-gray font-size-14');
  text.textContent = newsDatas[id].text;
  news.appendChild(text);

}

setData(0);

function changeNews(id) {
  setLeftData(id);
}

let newsMenuBtn = document.querySelector('.mobile-menu-news-items-btns');
let news__items = document.querySelector('.news__items');

let newsItemsBtnOff = document.querySelector(".news-items-menu-off"); // hamburger
let newsItemsBtnOn = document.querySelector(".news-items-menu-on"); // X

let newsItemsBtn = false;

newsMenuBtn.addEventListener("click", function() {

  if(newsItemsBtn == false) {
    newsItemsBtn = true;
    newsItemsBtnOff.style.display = "none";
    newsItemsBtnOn.style.display = "block";
  } else {
    newsItemsBtnOn.style.display = "none";
    newsItemsBtnOff.style.display = "block";
    newsItemsBtn = false;
  }
  news__items.classList.toggle("show");
});

const screenWidth = window.innerWidth;

document.addEventListener("click", function(e) {
  if(!newsMenuBtn.contains(e.target) && (screenWidth <= 768)) {
    newsItemsBtnOn.style.display = "none";
    newsItemsBtnOff.style.display = "block";
    newsItemsBtn = false;
    news__items.classList.remove("show");
  }
});
